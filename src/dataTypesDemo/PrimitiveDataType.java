package dataTypesDemo;

public class PrimitiveDataType {

	public static void main(String[] args) {

		byte a = 122;
		short b = 135;
		int c = 400;
				
		
		double e = 12.34;
		float f = 34.5f;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		System.out.println(e);
		System.out.println(f);
		
		
	}

}
